// equal operator
console.log(2 == 2);    // returns true 
console.log(2 == "2");  // returns true 
// strictly equal operator
console.log(2 === 2);   // returns true 
console.log(2 === "2"); // returns false as "2" is a string 
// not equal operator
console.log(3 != 2);    // returns true
console.log("hello" != "Hello");  // returns true
// strictly not equal to operator
console.log(2 !== 2)    // returns false 
console.log(2 !== "2")  // returns true (int not equal to string)
// logical and 
console.log(true && true)    //  1*1 = 1 - true
console.log(true && false)   //  1*0 = 0 - false
// logical or
console.log(true || false)   // 1+0 = 1 - true 
